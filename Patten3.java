public class Patten3 {
	
	public static void main(String[] args) 
	{
		 
	            int i, j, k = 0;
	            for (i = 1; i <= 4; i++) {
	                for (j = 1; j<= i; j++) {
	                    System.out.print(++k);
	                }
	       
	                System.out.println();
	            }
	        }
			
				
	}


